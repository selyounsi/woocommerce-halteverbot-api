
<div class="wrap">
    <h1 class="wp-heading-inline">Halteverbot Management Plugin für WooCommerce</h1>

    <div class="postbox-container">
        <div class="postbox">
            <div class="inside">
                <p>
                    Dieses Plugin erweitert WooCommerce um spezifische Funktionen zur Verwaltung von Halteverbotszonen und integriert sich nahtlos mit der zugehörigen App.
                </p>
                <h2>Hauptfunktionen:</h2>
                <ul>
                    <li>
                        <strong>Benutzerdefinierte Bestellfelder:</strong> Ermöglicht die Hinzufügung spezifischer Felder zu Bestellungen, wie Verkehrssicherungsmaßnahmen, Aufstellprotokolle und zusätzliche Dokumente (z. B. Anträge, Genehmigungen, Ablehnungen, Negativlisten).
                    </li>
                    <li>
                        <strong>E-Mail-Integration:</strong> Erstellt zwei neue E-Mail-Klassen für Angebote und Rechnungen, die über die App generiert und mittels der E-Mail-Funktionalität von WooCommerce/WordPress versendet werden.
                    </li>
                </ul>
                <h2>Integration mit der App:</h2>
                <p>
                    Das Plugin modifiziert die WooCommerce-Seite, um eine nahtlose Verbindung mit der App zu ermöglichen. Es ist erforderlich, zunächst API-Zugangsdaten in WooCommerce zu erstellen und diese in der App zu hinterlegen, um eine sichere und effiziente Synchronisation zwischen der App und der Website zu gewährleisten.
                </p>
                <p>
                    Weitere Informationen und den Quellcode finden Sie im <a href="https://github.com/selyounsi/woocommerce-halteverbot-api" target="_blank">GitHub-Repository</a>.
                </p>
            </div>
        </div>
    </div>
</div>


